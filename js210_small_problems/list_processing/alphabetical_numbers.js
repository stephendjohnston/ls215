const NUMBER_WORDS = ['zero', 'one', 'two', 'three', 'four', 'five',
                      'six', 'seven', 'eight', 'nine', 'ten', 'eleven',
                      'twelve', 'thirteen', 'fourteen', 'fifteen',
                      'sixteen', 'seventeen', 'eighteen', 'nineteen'];

function alphabeticNumberSort(numbers) {
  let sortedWords = NUMBER_WORDS.slice().sort();
  const result = [];
  

  numbers.forEach((num, index) => {
    result[sortedWords.indexOf(NUMBER_WORDS[index])] = index; // on first iteration: result[19] = 0 because NUMBER_WORDS[0] => 'zero' and sortedWords.indexOf('zero') => 19
  });                                                         // so result is => [ <18 empty spaces>, 0]

  return result;
}

